        -- CREATE TABLE - BOOK
        CREATE TABLE BOOK(
        BOOK_ID VARCHAR(20) NOT NULL,
        BOOK_NAME VARCHAR(50) NOT NULL,
        AUTHOR VARCHAR(20) NOT NULL,
        ISBN VARCHAR(13) NOT NULL,
        CATEGORY VARCHAR(20),
        LOCAT VARCHAR(20),
        PRIMARY KEY(BOOK_ID)
        );

        -- CREATE TABLE - STAT(STATUS)
        CREATE TABLE STAT(
        BOOK_ID VARCHAR(50) NOT NULL,
        IS_BORROWED VARCHAR(5),
        IS_RESERVED VARCHAR(5),
        RESERVE_ID INT,
        PRIMARY KEY(BOOK_ID),
        FOREIGN KEY(BOOK_ID) REFERENCES BOOK ON DELETE CASCADE
        );

	
        -- CREATE TABLE - PUBLISHER
        CREATE TABLE PUBLISHER(
		P_ID VARCHAR(50) NOT NULL,
        DATE_OF_CREATION DATE,
        P_NAME VARCHAR(20) NOT NULL,
        PRIMARY KEY(P_ID));

        -- CREATE TABLE - PATRON'S ACCOUNT
        CREATE TABLE PATRON_ACCOUNT(
        ACCOUNT_ID VARCHAR(50) NOT NULL,
        ACCOUNT_PASSWORD VARCHAR(20) NOT NULL,
        EMAIL VARCHAR(50) NOT NULL,
        IS_DEACTIVATED VARCHAR(20),
        DEACTIVATED_TIME DATE,
        PRIMARY KEY(ACCOUNT_ID)
        );

        -- CREATE TABLE - BORROW_ORDER
        CREATE TABLE BORROW_ORDER(
        B_ORDER_ID INT GENERATED BY DEFAULT AS IDENTITY,
        BOOK_ID VARCHAR(50) NOT NULL,
        ACCOUNT_ID VARCHAR(50) NOT NULL,
        IS_RETURNED VARCHAR(20),
        DUE_TIME DATE,
        NOTE_TIME DATE,
        FOREIGN KEY(BOOK_ID) REFERENCES BOOK(BOOK_ID) ON DELETE CASCADE,
        FOREIGN KEY(ACCOUNT_ID) REFERENCES PATRON_ACCOUNT(ACCOUNT_ID) ON DELETE CASCADE
        );

        -- CREATE TABLE - RESERVE_ORDER
        CREATE TABLE RESERVE_ORDER(
        R_ORDER_ID INT GENERATED BY DEFAULT AS IDENTITY,
        BOOK_ID VARCHAR(50) NOT NULL,
        ACCOUNT_ID VARCHAR(50) NOT NULL,
        CANCEL_TIME DATE,
        IS_CANCELED VARCHAR(20),
        IS_BORROWED VARCHAR(20),
        FOREIGN KEY(BOOK_ID) REFERENCES BOOK(BOOK_ID) ON DELETE CASCADE,
        FOREIGN KEY(ACCOUNT_ID) REFERENCES PATRON_ACCOUNT(ACCOUNT_ID) ON DELETE CASCADE
        );

        -- CREATE TABLE - MANAGER
        CREATE TABLE MANAGER(
        M_ID VARCHAR(50) NOT NULL,
        M_PASSWORD VARCHAR(20) NOT NULL
        );

        -- CREATE TABLE -BOOK_RECORD
        CREATE TABLE BOOK_RECORD(
        BOOK_ID VARCHAR(50) NOT NULL,
        BORROW_TIMES INT,
        DESIRE_TIMES INT,
        RESERVE_TIMES INT,
        FOREIGN KEY (BOOK_ID) REFERENCES BOOK);

        -- CREATE TABLE --DESIRE
